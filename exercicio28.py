op2 = (3 * num1) + num3
op3 = num3 ** 3

print("O produto do dobro do primeiro com metade do segundo é:", op1)
print("A soma do triplo do primeiro com o terceiro é:", op2)
print("O terceiro elevado ao cubo é:", op3)
